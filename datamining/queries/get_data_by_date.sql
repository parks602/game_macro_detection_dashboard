-- ============================================================
-- 쿼리 설명: 주어진 날짜에 해당하는 모든 데이터를 조회
-- 입력 파라미터: 
--   {table_name} - 테이블 이름
--   {date} - 날짜 (형식: 'YYYY-MM-DD')
-- 출력 필드:
--   모든 컬럼 (*)
-- 조건:
--   해당 테이블, 날짜에 속하는 모든 데이터를 조회
-- ============================================================

SELECT *
FROM {table_name}
WHERE Date = '{date}';
